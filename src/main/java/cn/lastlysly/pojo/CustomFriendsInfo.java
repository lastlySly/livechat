    public void setCustomFriendsHeadportrait(String customFriendsHeadportrait) {
        this.customFriendsHeadportrait = customFriendsHeadportrait;
    }

    public Boolean getCustomFriendsGender() {
        return customFriendsGender;
    }

    public void setCustomFriendsGender(Boolean customFriendsGender) {
        this.customFriendsGender = customFriendsGender;
    }

    public Integer getCustomFriendsAge() {
        return customFriendsAge;
    }

    public void setCustomFriendsAge(Integer customFriendsAge) {
        this.customFriendsAge = customFriendsAge;
    }
